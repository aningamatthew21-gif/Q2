-- TABLE 8: QUOTES
-- ==========================
CREATE TABLE QA_QUOTES (
    QUOTE_ID           VARCHAR2(50)   NOT NULL,
    STATUS             VARCHAR2(20)   DEFAULT 'DRAFT',
    CREATED_BY         VARCHAR2(255),
    CUSTOMER_ID        VARCHAR2(50),
    CUSTOMER_NAME      VARCHAR2(500),
    QUOTE_DATE         VARCHAR2(20),
    EXPIRES_AT         VARCHAR2(50),
    INCOTERM           VARCHAR2(20),
    CURRENCY           VARCHAR2(10)   DEFAULT 'GHS',
    NOTES              VARCHAR2(4000),
    TERMS              VARCHAR2(4000),
    SUBTOTAL           NUMBER(15,4)   DEFAULT 0,
    SHIPPING_CHARGE    NUMBER(15,4)   DEFAULT 0,
    INSURANCE_CHARGE   NUMBER(15,4)   DEFAULT 0,
    ORDER_DISCOUNT     NUMBER(15,4)   DEFAULT 0,
    HANDLING_CHARGE    NUMBER(15,4)   DEFAULT 0,
    TAX                NUMBER(15,4)   DEFAULT 0,
    TOTAL              NUMBER(15,4)   DEFAULT 0,
    GROSS_MARGIN_PCT   NUMBER(10,4),
    AUDIT_COMPUTED_BY  VARCHAR2(100),
    AUDIT_COMPUTED_AT  TIMESTAMP,
    AUDIT_VALIDATED    NUMBER(1)      DEFAULT 0,
    CONVERTED_TO_INV   VARCHAR2(50),
    CONVERSION_DATE    TIMESTAMP,
    CREATED_AT         TIMESTAMP      DEFAULT SYSTIMESTAMP,
    UPDATED_AT         TIMESTAMP      DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_QUOTES PRIMARY KEY (QUOTE_ID),
    CONSTRAINT FK_QT_CUSTOMER FOREIGN KEY (CUSTOMER_ID)
        REFERENCES QA_CUSTOMERS(CUSTOMER_ID)
);

CREATE INDEX IDX_QT_CUSTOMER  ON QA_QUOTES(CUSTOMER_ID);
CREATE INDEX IDX_QT_CREATED   ON QA_QUOTES(CREATED_BY);
CREATE INDEX IDX_QT_STATUS    ON QA_QUOTES(STATUS);

