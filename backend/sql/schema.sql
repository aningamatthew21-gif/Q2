-- ==========================
-- TABLE 1: USERS
-- ==========================
CREATE TABLE QA_USERS (
    USER_EMAIL    VARCHAR2(255)  NOT NULL,
    USER_ROLE     VARCHAR2(20)   DEFAULT 'sales'  NOT NULL,
    USER_NAME     VARCHAR2(255),
    USER_STATUS   VARCHAR2(20)   DEFAULT 'active',
    CREATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_USERS PRIMARY KEY (USER_EMAIL),
    CONSTRAINT CHK_USERS_ROLE CHECK (USER_ROLE IN ('sales', 'controller'))
);

-- ==========================
-- TABLE 2: OTPS
-- ==========================
CREATE TABLE QA_OTPS (
    OTP_EMAIL     VARCHAR2(255)  NOT NULL,
    OTP_CODE      NUMBER(6)      NOT NULL,
    CREATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP,
    EXPIRES_AT    TIMESTAMP      DEFAULT (SYSTIMESTAMP + INTERVAL '10' MINUTE),
    CONSTRAINT PK_OTPS PRIMARY KEY (OTP_EMAIL)
);

-- ==========================
-- TABLE 3: CUSTOMERS
-- ==========================
CREATE TABLE QA_CUSTOMERS (
    CUSTOMER_ID       VARCHAR2(50)   NOT NULL,
    CUSTOMER_NAME     VARCHAR2(500)  NOT NULL,
    CONTACT_PERSON    VARCHAR2(255),
    CONTACT_EMAIL     VARCHAR2(255),
    LOCATION          VARCHAR2(255),
    PO_BOX            VARCHAR2(100),
    REGION            VARCHAR2(100),
    ADDRESS           VARCHAR2(500),
    NOTES             VARCHAR2(4000),
    CUSTOMER_SINCE    VARCHAR2(50),
    CREATED_AT        TIMESTAMP      DEFAULT SYSTIMESTAMP,
    UPDATED_AT        TIMESTAMP      DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_CUSTOMERS PRIMARY KEY (CUSTOMER_ID)
);

CREATE INDEX IDX_CUST_NAME   ON QA_CUSTOMERS(CUSTOMER_NAME);
CREATE INDEX IDX_CUST_EMAIL  ON QA_CUSTOMERS(CONTACT_EMAIL);
CREATE INDEX IDX_CUST_REGION ON QA_CUSTOMERS(REGION);

-- ==========================
-- TABLE 4: INVENTORY
-- ==========================
CREATE TABLE QA_INVENTORY (
    SKU                VARCHAR2(100)  NOT NULL,
    ITEM_NAME          VARCHAR2(500)  NOT NULL,
    VENDOR             VARCHAR2(255),
    STOCK              NUMBER(10,0)   DEFAULT 0,
    PRICE              NUMBER(15,4)   DEFAULT 0,
    RESTOCK_LIMIT      NUMBER(10,0)   DEFAULT 0,
    CURRENCY           VARCHAR2(10)   DEFAULT 'GHS',
    UNIT_COST          NUMBER(15,4),
    WEIGHT_KG          NUMBER(10,4),
    DIM_LENGTH         NUMBER(10,4),
    DIM_WIDTH          NUMBER(10,4),
    DIM_HEIGHT         NUMBER(10,4),
    FREIGHT_PER_UNIT   NUMBER(15,4)   DEFAULT 0,
    DUTY_PER_UNIT      NUMBER(15,4)   DEFAULT 0,
    INSURANCE_PER_UNIT NUMBER(15,4)   DEFAULT 0,
    PACKAGING_PER_UNIT NUMBER(15,4)   DEFAULT 0,
    OTHER_PER_UNIT     NUMBER(15,4)   DEFAULT 0,
    MARKUP_OVERRIDE    NUMBER(10,4),
    PRICING_TIER       VARCHAR2(20)   DEFAULT 'standard',
    UPDATED_AT         TIMESTAMP      DEFAULT SYSTIMESTAMP,
    UPDATED_BY         VARCHAR2(255),
    CONSTRAINT PK_INVENTORY PRIMARY KEY (SKU)
);

CREATE INDEX IDX_INV_NAME   ON QA_INVENTORY(ITEM_NAME);
CREATE INDEX IDX_INV_VENDOR ON QA_INVENTORY(VENDOR);
CREATE INDEX IDX_INV_STOCK  ON QA_INVENTORY(STOCK);

-- ==========================
-- SEQUENCE: Invoice Auto-Number
-- ==========================
CREATE SEQUENCE QA_INVOICE_SEQ
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;

-- ==========================
-- TABLE 5: INVOICES
-- ==========================
CREATE TABLE QA_INVOICES (
    INVOICE_ID           VARCHAR2(50)   NOT NULL,
    APPROVED_INVOICE_ID  VARCHAR2(50),
    SALESPERSON_ID       VARCHAR2(255),
    CREATED_BY           VARCHAR2(255),
    CUSTOMER_ID          VARCHAR2(50),
    CUSTOMER_NAME        VARCHAR2(500),
    INVOICE_DATE         VARCHAR2(20),
    CURRENCY             VARCHAR2(10)   DEFAULT 'GHS',
    EXCHANGE_RATE        NUMBER(15,6)   DEFAULT 1,
    SUBTOTAL             NUMBER(15,4)   DEFAULT 0,
    TOTAL                NUMBER(15,4)   DEFAULT 0,
    TAXES                NUMBER(15,4)   DEFAULT 0,
    TAX_BREAKDOWN        CLOB,
    STATUS               VARCHAR2(50)   DEFAULT 'Pending Pricing',
    SUBMITTED_AT         TIMESTAMP,
    APPROVED_AT          TIMESTAMP,
    APPROVED_BY          VARCHAR2(255),
    REJECTED_AT          TIMESTAMP,
    REJECTED_BY          VARCHAR2(255),
    REJECTION_REASON     VARCHAR2(4000),
    CUSTOMER_ACTION_AT   TIMESTAMP,
    SIGNATURE_DATA       CLOB,
    AMOUNT_PAID          NUMBER(15,4)   DEFAULT 0,
    BALANCE_DUE          NUMBER(15,4)   DEFAULT 0,
    CONVERTED_FROM_QUOTE VARCHAR2(50),
    CONVERSION_DATE      TIMESTAMP,
    CREATED_AT           TIMESTAMP      DEFAULT SYSTIMESTAMP,
    UPDATED_AT           TIMESTAMP      DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_INVOICES PRIMARY KEY (INVOICE_ID),
    CONSTRAINT FK_INV_CUSTOMER FOREIGN KEY (CUSTOMER_ID)
        REFERENCES QA_CUSTOMERS(CUSTOMER_ID)
);

CREATE INDEX IDX_INV_CUSTOMER   ON QA_INVOICES(CUSTOMER_ID);
CREATE INDEX IDX_INV_STATUS     ON QA_INVOICES(STATUS);
CREATE INDEX IDX_INV_DATE       ON QA_INVOICES(INVOICE_DATE);
CREATE INDEX IDX_INV_CREATED_BY ON QA_INVOICES(CREATED_BY);

-- ==========================
-- TABLE 6: INVOICE LINE ITEMS
-- ==========================
CREATE TABLE QA_INVOICE_LINE_ITEMS (
    LINE_ID    NUMBER         GENERATED ALWAYS AS IDENTITY,
    INVOICE_ID VARCHAR2(50)   NOT NULL,
    SKU        VARCHAR2(100),
    ITEM_NAME  VARCHAR2(500),
    QUANTITY   NUMBER(10,2)   DEFAULT 1,
    UNIT_PRICE NUMBER(15,4)   DEFAULT 0,
    LINE_TOTAL NUMBER(15,4)   DEFAULT 0,
    SORT_ORDER NUMBER(5,0)    DEFAULT 0,
    CONSTRAINT PK_INV_LINE_ITEMS PRIMARY KEY (LINE_ID),
    CONSTRAINT FK_LI_INVOICE FOREIGN KEY (INVOICE_ID)
        REFERENCES QA_INVOICES(INVOICE_ID) ON DELETE CASCADE
);

CREATE INDEX IDX_LI_INVOICE ON QA_INVOICE_LINE_ITEMS(INVOICE_ID);

-- ==========================
-- TABLE 7: INVOICE PAYMENTS
-- ==========================
CREATE TABLE QA_INVOICE_PAYMENTS (
