    AMOUNT           NUMBER(15,4)   NOT NULL,
    PAYMENT_DATE     VARCHAR2(20),
    PAYMENT_METHOD   VARCHAR2(100),
    REFERENCE_NUMBER VARCHAR2(255),
    CREATED_AT       TIMESTAMP      DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_INV_PAYMENTS PRIMARY KEY (PAYMENT_ID),
    CONSTRAINT FK_PAY_INVOICE FOREIGN KEY (INVOICE_ID)
        REFERENCES QA_INVOICES(INVOICE_ID) ON DELETE CASCADE
);

CREATE INDEX IDX_PAY_INVOICE ON QA_INVOICE_PAYMENTS(INVOICE_ID);

